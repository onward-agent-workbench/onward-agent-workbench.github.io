function setLanguage(lang) {
  document.documentElement.setAttribute('data-lang', lang);
  document.documentElement.lang = lang === 'zh-CN' ? 'zh' : 'en';

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  var toggle = document.getElementById('langToggle');
  if (toggle) {
    toggle.textContent = lang === 'en' ? '\u4e2d\u6587' : 'EN';
  }

  localStorage.setItem('onward-lang', lang);
}

function getCurrentLang() {
  return document.documentElement.getAttribute('data-lang') || 'en';
}

function toggleLanguage() {
  var current = getCurrentLang();
  setLanguage(current === 'en' ? 'zh-CN' : 'en');
}

(function() {
  var stored = localStorage.getItem('onward-lang');
  setLanguage(stored || 'en');
})();
